    <p class="texto">La Gioconda</p> 
    <p class="texto">El Retrato de Lisa Gherardini, esposa de Francesco del Giocondo, ​más conocido como La Gioconda o La Mona Lisa, es una obra pictórica del polímata renacentista italiano Leonardo da Vinci. Fue adquirida por el rey Francisco I de Francia a comienzos del siglo XVI y desde entonces es propiedad del Estado Francés. </p>
