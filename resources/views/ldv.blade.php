    <p class="texto">San Juan Bautista</p>
    <p class="texto">También conocido como "El Vato" Fue una de las últimas pinturas del autor y, sin duda, una de las más debatidas. Hay mucha polémica sobre el significado de la mano del santo apuntando hacia arriba, y su sonrisa enigmática ha provocado discusiones semejantes a las de la Mona Lisa.</p>
    <p class="texto">Representa a Juan el Bautista en la soledad del desierto. Está representado de medio cuerpo, una variación sobre el tema de la figura construida clásica y monumentalmente en el espacio en directa sugestión de los modelos de la estatuaria antigua.</p>
