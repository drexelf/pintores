   <h2 class="subtitulo"><a name="his">Pinturas</a></h2> 
    <br>
   
    <p class="texto">El Grito</p> 
    <p class="texto">El grito es el título de cuatro cuadros del noruego Edvard Munch. La versión más famosa se encuentra en la Galería Nacional de Noruega y fue completada en 1893.​ Otras dos versiones del cuadro se encuentran en el Museo Munch, también en Oslo, mientras que la cuarta versión pertenece a una colección particular.</p>
    <br>
    <div align="center">
    <img src="../img/edm1.jpg" width= "150px" ></div>
    <br>

    <p class="texto">Ansiedad</p> 
    <p class= "texto">Ansiedad es un óleo sobre lienzo pintado por el artista expresionista Edvard Munch en 1894. Está actualmente en el Museo Munch de Oslo, Noruega. Se considera que Ansiedad está estrechamente relacionada con la más famosa, El grito. El paisaje de fondo es el mismo en ambas, el fiordo de Oslo, visto desde Ekeberg.</p>
    <br>
    <div align="center">
    <img src="../img/edm2.jpg" width= "150px" ></div>
    <br>

    <p class="texto">Autorretrato con una botella de vino</p>
    <p class="texto">El “Autorretrato con una botella de vino” no es una representación de la apariencia física del pintor, Edvard Munch. Se trata, más bien, de una expresión psicológica de sus dilemas internos. Como tal, podemos llamar a esto un retrato de la psique apenada y atormentada del pintor.</p>
    <p class="texto">Por lo tanto, la imagen no se puede explicar como una fotografía, por las imágenes que transmite. Sólo puede ser entendido por la definición clara de lo que representa. Sentimientos como el miedo, la soledad, la desesperanza, la incertidumbre, y conceptos como la fobia, el aislamiento y la muerte son simbolizados por la técnica única del artista y los valores simbólicos de los pocos objetos discernibles en su pintura.</p>
    <br>
    <div align="center">
    <img src="../img/edm3.jpg" width="190px" height="180" ></div>
    <br>

    <p class="texto">Pubertad</p>
    <p class="texto">Pubertad es una pintura de 1894–95 de Edvard Munch, pintor noruego destacado en el arte expresionista. Pubertad también fue reproducida en litografía y aguafuerte por Munch. </p>
    <br>
