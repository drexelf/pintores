<h2 class="subtitulo">¿Quien es?</h2>

<br>
<div align="justify">
<p class="texto">Edvard Munch fue un pintor y grabador noruego. 
Sus evocativas obras sobre la angustia influyeron profundamente 
en el expresionismo alemán de comienzos del siglo XX. Sus obras 
han sido consideradas</p><br> 
<p class="texto"> El pintor decía de sí mismo que, del mismo modo 
que Leonardo da Vinci había estudiado la anatomía humana y 
diseccionado cuerpos, él intentaba diseccionar almas. Por ello, 
los temas más frecuentes en sus obras fueron los relacionados 
con los sentimientos y las tragedias humanas, como la soledad 
(Melancolía), la angustia (El grito), la muerte (Muerte de un bohemio) 
y el erotismo (Amantes, El beso). Se le considera precursor 
del expresionismo, por la fuerte expresividad de los rostros y 
las actitudes de sus figuras, además del mejor pintor noruego 
de todos los tiempos.</p>
