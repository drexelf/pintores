    <p class="texto">Amor y dolor</p>
    <p class="texto">Amor y Dolor es una pintura de Edvard Munch, también a menudo denominada Vampiro, aunque no por Munch.</p>
    <p class="texto">La pintura muestra una mujer con largos cabellos rojos besando a un hombre en el cuello, mientras la pareja se abraza.A pesar de que muchos han visto en ello a "un hombre encerrado en el abrazo torturado de un vampiro – el lacio cabello rojo cayendo sobre la suave piel desnuda" Munch siempre reclamó que no mostraba nada más que "una mujer besando a un hombre en el cuello".</p>
