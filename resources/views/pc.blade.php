    <p class="texto">La mujer que llora</p> 
    <p class="texto">La mujer que llora es un óleo sobre lienzo pintado por Pablo Picasso en Francia en 1937. Picasso estaba intrigado con el tema y revisó el tema varias veces ese año. Esta pintura, creada el 26 de octubre de 1937, fue la más elaborada de la serie. Sus dimensiones son 60 х 49 cm, 23 19 х 19 ¼ pulgadas.</p>
