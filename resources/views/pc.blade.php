    <p class="texto">Las señoritas de Avignon</p> 
    <p class="texto">Las señoritas de Avignon, Las señoritas de Aviñón o, más correctamente, Las señoritas de la calle de Avinyó, ​ es un cuadro del pintor español Pablo Picasso pintado en 1907 al óleo sobre lienzo y sus medidas son 243,9 x 233,7 cm. Se conserva en el Museo de Arte Moderno de Nueva York.</p>
