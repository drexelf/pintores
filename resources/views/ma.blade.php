    <p class="texto">Crucifixión</p> 
    <p class="texto">La Crucifixión de San Pedro es un fresco del pintor renacentista italiano Miguel Ángel. Fue ejecutado entre 1546 y 1550 en la pared de la Capilla Paulina del Palacio Apostólico, en la Ciudad del Vaticano. Es un fresco que mide 6,25 metros de alto y 6,62 metros de ancho.</p>
