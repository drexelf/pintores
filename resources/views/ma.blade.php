<h2 class="subtitulo"><a name="his">Pinturas</a></h2> 
    <br>
   
    <p class="texto">Santo Entierro</p> 
    <p class="texto">El Santo Entierro es un cuadro inacabado atribuido al pintor renacentista italiano Miguel Ángel. La datación es alrededor de 1500-1501. Se usó una técnica mixta. Mide 162 centímetros de alto y 150 cm. de ancho. Se conserva en la National Gallery de Londres, Reino Unido</p>
    <br>
    <div align="center">
    <img src="../img/map1.jpg" width= "160px" height="180px" ></div>
    <br>


    <p class="texto">Tondo Doni</p> 
    <p class="texto">La Sagrada Familia, conocida como Tondo Doni, es un cuadro del pintor renacentista italiano Miguel Ángel. Su diámetro es de 120 centímetros. Se considera que se ejecutó entre 1503 y 1504 o, con mayor probabilidad, en 1506.</p>
    <br>
    <div align="center">
    <img src="../img/map2.jpg" width= "150px" ></div>
    <br>

    <p class="texto">Bóveda de la Capilla Sixtina</p> 
    <p class="texto">La bóveda de la Capilla Sixtina de Roma es un conjunto de pinturas al fresco realizadas para decorar la bóveda de la Capilla Sixtina, en la Ciudad del Vaticano.</p>
    <br>
    <div align="center">
    <img src="../img/map3.jpg"  width= "300px" height="150px"></div>
